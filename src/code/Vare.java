package code;

public class Vare {
    private int id;
    private String name;
    private int lastPriceChange;
    private int price;
    private int content;
    private String unit;

    public Vare(int id, String name, int lastPriceChange, int price, int content, String unit){
        this.id = id;
        this.name = name;
        this.lastPriceChange = lastPriceChange;
        this.price = price;
        this.content = content;
        this.unit = unit;

    }

}
